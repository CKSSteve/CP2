package com.uxb2b.ecp.bean;

import java.util.Date;
import java.util.List;

public class QueryTenderBean {

	private String issuerId;
	private String inviteDateStartDate;
	private String inviteDateEndDate;
	private String closeDateTimeStartDate;
	private String closeDateTimeEndDate;
	private String currencyId;
	
	private	String quotesDateStartDate;
	private	String quotesDateEndDate;
	private List<String> statusTypeArray;
	private List<String> approveStatusTypeArray;

	
	public QueryTenderBean(){
		
	}
	
	public QueryTenderBean(String issuerId,String inviteDateStartDate,String inviteDateEndDate,String closeDateTimeStartDate,String closeDateTimeEndDate,String currencyId){
		
		this.issuerId=issuerId;
		this.inviteDateStartDate=inviteDateStartDate;
		this.inviteDateEndDate=inviteDateEndDate;
		this.closeDateTimeStartDate=closeDateTimeStartDate;
		this.closeDateTimeEndDate=closeDateTimeEndDate;
		this.currencyId=currencyId;
	}
	
	public String getIssuerId() {
		return issuerId;
	}

	public void setIssuerId(String issuerId) {
		this.issuerId = issuerId;
	}

	public String getInviteDateStartDate() {
		return inviteDateStartDate;
	}

	public void setInviteDateStartDate(String inviteDateStartDate) {
		this.inviteDateStartDate = inviteDateStartDate;
	}

	public String getInviteDateEndDate() {
		return inviteDateEndDate;
	}

	public void setInviteDateEndDate(String inviteDateEndDate) {
		this.inviteDateEndDate = inviteDateEndDate;
	}

	public String getCloseDateTimeStartDate() {
		return closeDateTimeStartDate;
	}

	public void setCloseDateTimeStartDate(String closeDateTimeStartDate) {
		this.closeDateTimeStartDate = closeDateTimeStartDate;
	}

	public String getCloseDateTimeEndDate() {
		return closeDateTimeEndDate;
	}

	public void setCloseDateTimeEndDate(String closeDateTimeEndDate) {
		this.closeDateTimeEndDate = closeDateTimeEndDate;
	}

	public String getCurrencyId() {
		return currencyId;
	}

	public void setCurrencyId(String currencyId) {
		this.currencyId = currencyId;
	}

	public String getQuotesDateStartDate() {
		return quotesDateStartDate;
	}

	public void setQuotesDateStartDate(String quotesDateStartDate) {
		this.quotesDateStartDate = quotesDateStartDate;
	}

	public String getQuotesDateEndDate() {
		return quotesDateEndDate;
	}

	public void setQuotesDateEndDate(String quotesDateEndDate) {
		this.quotesDateEndDate = quotesDateEndDate;
	}

	public List<String> getStatusTypeArray() {
		return statusTypeArray;
	}

	public void setStatusTypeArray(List<String> statusTypeArray) {
		this.statusTypeArray = statusTypeArray;
	}

	public List<String> getApproveStatusTypeArray() {
		return approveStatusTypeArray;
	}

	public void setApproveStatusTypeArray(List<String> approveStatusTypeArray) {
		this.approveStatusTypeArray = approveStatusTypeArray;
	}
	
}

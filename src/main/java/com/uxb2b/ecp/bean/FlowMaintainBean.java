package com.uxb2b.ecp.bean;

import java.util.List;

import com.uxb2b.ecp.model.Function;

/**
 * 簽核流程維護
 * @author evayang
 *
 */
public class FlowMaintainBean {

	private String flowName;
	private String flowMemo;
	private String[] flowFunctionId;
	private String[] roleId;
	private String flowId;
	private Boolean alreadyDel;
	private String action;
	
	private List<Function> functions;
	
	public String getFlowName() {
		return flowName;
	}
	public void setFlowName(String flowName) {
		this.flowName = flowName;
	}
	public String getFlowMemo() {
		return flowMemo;
	}
	public void setFlowMemo(String flowMemo) {
		this.flowMemo = flowMemo;
	}
	
	public String[] getFlowFunctionId() {
		return flowFunctionId;
	}
	public void setFlowFunctionId(String[] flowFunctionId) {
		this.flowFunctionId = flowFunctionId;
	}
	
	public String[] getRoleId() {
		return roleId;
	}
	
	public void setRoleId(String[] roleId) {
		this.roleId = roleId;
	}
	
	public String getFlowId() {
		return flowId;
	}
	
	public void setFlowId(String flowId) {
		this.flowId = flowId;
	}
	public Boolean getAlreadyDel() {
		return alreadyDel;
	}
	public void setAlreadyDel(Boolean alreadyDel) {
		this.alreadyDel = alreadyDel;
	}
	public List<Function> getFunctions() {
		return functions;
	}
	public void setFunctions(List<Function> functions) {
		this.functions = functions;
	}
	public String getAction() {
		return action;
	}
	public void setAction(String action) {
		this.action = action;
	}
}

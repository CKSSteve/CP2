package com.uxb2b.ecp.bean;

import java.util.List;

public class QueryQuotesBean {

	private String issuerId;
	private	String quotesDateStartDate;
	private	String quotesDateEndDate;
	private List<String> statusTypeArray;
	private List<String> approveStatusTypeArray;
	private String currencyId;
	
	public QueryQuotesBean(){}
	
	public QueryQuotesBean(String issuerId,String quotesDateStartDate,String quotesDateEndDate,List<String> statusTypeArray,List<String> approveStatusTypeArray,String currencyId){
		
		this.issuerId=issuerId;
		this.quotesDateStartDate=quotesDateStartDate;
		this.quotesDateEndDate=quotesDateEndDate;
		this.statusTypeArray=statusTypeArray;
		this.approveStatusTypeArray=approveStatusTypeArray;
		
	}

	public String getIssuerId() {
		return issuerId;
	}

	public void setIssuerId(String issuerId) {
		this.issuerId = issuerId;
	}

	public String getQuotesDateStartDate() {
		return quotesDateStartDate;
	}

	public void setQuotesDateStartDate(String quotesDateStartDate) {
		this.quotesDateStartDate = quotesDateStartDate;
	}

	public String getQuotesDateEndDate() {
		return quotesDateEndDate;
	}

	public void setQuotesDateEndDate(String quotesDateEndDate) {
		this.quotesDateEndDate = quotesDateEndDate;
	}

	public List<String> getStatusTypeArray() {
		return statusTypeArray;
	}

	public void setStatusTypeArray(List<String> statusTypeArray) {
		this.statusTypeArray = statusTypeArray;
	}

	public List<String> getApproveStatusTypeArray() {
		return approveStatusTypeArray;
	}

	public void setApproveStatusTypeArray(List<String> approveStatusTypeArray) {
		this.approveStatusTypeArray = approveStatusTypeArray;
	}

	public String getCurrencyId() {
		return currencyId;
	}

	public void setCurrencyId(String currencyId) {
		this.currencyId = currencyId;
	}
	
}

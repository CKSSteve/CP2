package com.uxb2b.ecp.types;

/**
 * 取得Table:SYS_CODE-->code_type類型 
 * 
 * @author takashi
 *
 */
public enum ActionAndStatusType {
	ACTION_LOGIN("Login"), //登入
	ACTION_MODIFY("Modify"), //修改
	ACTION_ADD("Add"), //新增
	ACTION_DELETE("Delete"), //刪除
	STATUS_SUCCESS("Success"), //成功
	STATUS_ERROR("Error"); //失敗
	
	private String value;
	
	private ActionAndStatusType(String var) {
		this.value = var;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
	
}
